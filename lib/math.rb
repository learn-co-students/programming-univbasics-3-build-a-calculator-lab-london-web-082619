def addition(num1, num2)
x = num1 + num2
return x
end

def subtraction(num1, num2)
y = num1 - num2
return y
end

def division(num1, num2)
z = num1/num2
return z
end

def multiplication(num1, num2)
a = num1*num2
return a
end

def modulo(num1, num2)
b = num1.modulo(num2)
return b
end

def square_root(num)
c = Math.sqrt(num)
return c
end
